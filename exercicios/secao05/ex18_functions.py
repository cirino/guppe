def sumnumbers(a: float, b: float) -> float:
    """
    Takes in a and b and sum them
    :param a: number
    :param b: number
    :return: float number of a + b
    """
    return a + b


def subtract(a: float, b: float) -> float:
    """
    Takes in a and subtracts b from it
    :param a: float number
    :param b: another float number
    :return: float number of a - b
    """
    return a - b


def multiply(a: float, b: float) -> float:
    """
    Takes a and multiply by b
    :param a: float number
    :param b: float number
    :return: float number of a * b
    """
    return a * b


def divide(a, b):
    """
    Takes in a and divide it by b
    :param a: float number
    :param b: float number
    :return: float number of a / b
    """
    return a / b
